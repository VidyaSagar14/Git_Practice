class CharAlpha{
	public static void main(String args[]){
		char init1='s';
		char init2='b';
		System.out.println(init1+","+init2);
		System.out.println("Printing the characters in ascending order:-");
		if(init1>init2)
			System.out.println(init2+","+init1);
		else
			System.out.println(init1+","+init2);
	}
}